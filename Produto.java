class Produto {
	private double Peso;
	private double Preço;
	private String nome;
	
	public double getPeso() {
		return this.Peso;
	}
	
	public void setPeso(double p) {
		this.Peso = p;
	}
	
	public double getPreço() {
		return this.Preço;
	}
	
	public void setPreço(double p) {
		this.Preço = p;
	}
	
	public String getNome() {
		return this.nome;
	}
	
	public void setNome(String n)  {
		this.nome = n;
	}
}
